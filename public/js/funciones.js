function get_data(parametros, response) {
    $.ajax({
        method: "GET",
        url: SERVER_API,
        data: parametros
    })
        .done(function( res ) {
            response(res);
        });
}
function validar_url(s) {
    var regexp = /(ftp|http|https):\/\/(\w+:{0,1}\w*@)?(\S+)(:[0-9]+)?(\/|\/([\w#!:.?+=&%@!\-\/]))?/
    return regexp.test(s);
}

